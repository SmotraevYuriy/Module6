package Medium1;

public class Calculator {
    double a;
    double b;

    public double addition(double a, double b) {
        return a + b;
    }
    public double subtraction(double a, double b) {
        return a - b;
    }
    public double division(double a, double b) {
        return a / b;
    }
    public double multiplication(double a, double b) {
        return a * b;
    }
}
