package Medium1;

public class Iterations {
    double a;
    double b;

    public Iterations(double b, double a) {
        this.b = b;
        this.a = a;
    }
    public double addition() {
        return a + b;
    }
    public double subtraction() {
        return a - b;
    }
    public double division() {
        return a / b;
    }
    public double multiplication() {
        return a * b;
    }
}
